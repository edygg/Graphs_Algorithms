
package edu.unitec.adt;

/**
 * Enumeración que contiene constantes que identifican cada recorrido para los
 * arboles.
 * @author EdilsonFernando
 */
public enum Journeys {
    /**
     * Recorrido en orden simétrico.
     */
    INORDER,
    /**
     * Recorrido en orden previo.
     */
    PREORDER,
    /**
     * Recorrido en orden posterior.
     */
    POSTORDER;
}
